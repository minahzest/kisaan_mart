var lang = '';
var type = '';

function selectLanguage(id){
	lang = id;
	if (id == 1) {
		toastr.info("English");
	}else if(id == 2){
		toastr.info("Urudu");
	}
}
function selectType(id){
	type = id;
	if (id == 1) {
		toastr.info("Farmer");
	}else if(id == 2){
		toastr.info("Buyer");
	}
	$('#next').removeClass('disabled');
}

function checkSection(){
	if($('#account').css('display') == 'block'){
		$('#next').addClass('disabled');
		$('#submit').removeClass('disabled');

		if ($('#phone').val() == "") {
			toastr.error("Mobile number is required");
		}else{
			$.ajax({
				type: "POST",
				url: "<?=base_url()?>getOTP",
				data: 'phone='+$("#phone").val(),
				success: function(result){
					var res = $.parseJSON(result);
					if (res.res['status'] == 'success') {
						toastr.success(res.res['message']+" "+res.otp);
					}else if(res.res['status'] == 'error'){
						toastr.error(res.res['message']);
					}
				},
				error: function(result){  
					toastr.error(result);
				}
			});
		}
	}else{
		$('#next').removeClass('disabled');
		$('#submit').addClass('disabled');
	}

	if ($('#lan').css('display') == 'block') {
		$('#next').addClass('disabled');
	}
}

//sign up form submission
$('#signUp').on('submit', function (e) {
if (!(e.isDefaultPrevented())) {
  e.preventDefault();
	var data = $("#signUp").serialize();
	$.ajax({
		type: 'POST',
		url: '<?=base_url()?>sign_up',
		data: data+"&type="+type+"&lang="+lang,
		success: function (result){  
			var res = $.parseJSON(result);
			if (res.res['status'] == 'success') {
				toastr.success(res.res['message']);
				if (type == 1) {
					$("#successMsg").html("");
				}else{
					$("#successMsg").html("Your profile will getapproved in 24 hours");
				}
				$("#next").click();
				$("#next").addClass("disabled");
				$("#prev").addClass("disabled");
			}else if(res.res['status'] == 'error'){
				toastr.error(res.res['message']);
			}
		},
		error: function (result){  
			toastr.error(result);
		}
	});
}
